# Turtle Graphics

**Turtle graphics** is an amusing way to learn introductory programming by drawing vector images. The idea had been developed in the 1960s as part of the ((Logo|https://en.wikipedia.org/wiki/Logo_(programming_language))) programming language.

Imagine that a robotic turtle is crawling over a canvas. The turtle holds a pen and the pen leaves behind a trail on the canvas.

The turtle is guided by a program. There are commands that control how the turtle moves and turns, commands that control the pen, and so on.

There are many implementations of Turtle Graphics. They generally use Logo, C, Python or similar programming languages.

**Arty the Turtle** (or simply: **Arty**) is also one of turtle graphics implementations. The language it uses is a variant of ((Forth|https://en.wikipedia.org/wiki/Forth_(programming_language))).
